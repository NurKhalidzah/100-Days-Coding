def penjumlahan(a, b):
  return a + b
def pengurangan(a, b):
  return a - b
def perkalian(a, b):
  return a * b
def pembagian(a, b):
  return a / b
def sisa_bagi(a, b):
  return a % b
a = int(input("angka 1: "))
b = int(input("angka 2: "))
print("Penjumlahan:", penjumlahan(a, b))
print("Pengurangan:", pengurangan(a, b))
print("Perkalian:", perkalian(a, b))
print("Pembagian:", pembagian(a, b))
print("Sisa Bagi:", sisa_bagi(a, b))
